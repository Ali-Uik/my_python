def qazo(n):
    day = n * 365
    bomdod = 2 * day
    peshin = 4 * day
    asr = 4 * day
    shom = 3 * day
    xufton = 4 * day
    vitr = 3 * day
    return f'Umumiy qazo qilingan namoz kunlari - {day}\nBomdod-{bomdod} rakat\nPeshin-{peshin} rakat\nAsr-{asr} rakat\n' \
           f'Shom-{shom} rakat\nXufton-{xufton} rakat\nVitr vojib-{vitr} rakat '
